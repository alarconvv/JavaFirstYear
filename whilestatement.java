// While is a form of loops, other are do and for
// while is the most basic form. 
// it is used for repetitions. If the first statement is false, while will be never executed
//

public class whilestatement
{
    public static void main( String [] args)
    {
        int x = 10;
        
        while(x != 0)
        {
            System.out.println(x);
            x = x - 1;
        }


    }
}